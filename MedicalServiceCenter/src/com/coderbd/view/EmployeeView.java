    public void displayMedicalAtComboBox() {
        MedicalDao dao = new MedicalDaoImpl();
        List<Medical> list = dao.getList();
        cmbMedical.addItem("Select A List");
        for (Medical medical : list) {
            cmbMedical.addItem(medical.getName());
        }
    }

    public void displayEmployeeTypeAtComboBox() {
        EmployeeTypeDao dao = new EmployeeTypeDaoImpl();
        List<EmployeeType> list = dao.getList();
        cmbEmployeeType.addItem("Select A Type");
        for (EmployeeType employeeType : list) {
            cmbEmployeeType.addItem(employeeType.getType());
        }
    }

    public void displayEmployeeListIntoTable() {
        clearTable();

        EmployeeDao dao = new EmployeeDaoImpl();
        List<Employee> list = dao.getList();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] cols = new Object[12];

        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getFastName();
            cols[2] = list.get(i).getLastName();
            cols[3] = list.get(i).getJoiningDate();
            cols[4] = list.get(i).getAge();
            cols[5] = list.get(i).getFee();
            cols[6] = list.get(i).getAddress();
            cols[7] = list.get(i).getSpeciality();
            cols[8] = list.get(i).getEducationalQualification();
            cols[9] = list.get(i).getMobileNumber();
            cols[10] = list.get(i).getEmployeeType();
            cols[11] = list.get(i).getMedical();

            model.addRow(cols);
        }
    }

    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
    }

