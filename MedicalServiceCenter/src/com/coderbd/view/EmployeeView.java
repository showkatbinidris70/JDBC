    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        EmployeeDao employeeDao = new EmployeeDaoImpl();
        LocalDate todayLocalDate = LocalDate.now(ZoneId.of("America/Montreal"));
        java.sql.Date sqlDate = java.sql.Date.valueOf(todayLocalDate);

        String fName = txtFastName.getText().trim();
        String lName = txtLastName.getText().trim();
        int age = Integer.parseInt(txtAge.getText().trim());
        Double fee = Double.parseDouble(txtFee.getText().trim());
        String addrss = txtAddress.getText().trim();
        String speciality = txtSpeciality.getText().trim();
        String eduQualification = txtEducationalQualification.getText().trim();
        String mobile = txtMobileNumber.getText().trim();
        String empType = cmbEmployeeType.getItemAt(cmbEmployeeType.getSelectedIndex());
        String medical = cmbMedical.getItemAt(cmbMedical.getSelectedIndex());

        /*
       (String fastName, String lastName, Date joiningDate, int age, double fee, 
        String address, String speciality, String educationalQualification, 
        String mobileNumber, EmployeeType employeeType, Medical medical) 
         */
        Employee employee = new Employee(txtFastName.getText().trim(), txtLastName.getText().trim(),
                sqlDate, Integer.parseInt(txtAge.getText().trim()), Double.parseDouble(txtFee.getText().trim()),
                txtAddress.getText().trim(), txtSpeciality.getText().trim(), txtEducationalQualification.getText().trim(),
                txtMobileNumber.getText().trim(), new EmployeeType(empType), new Medical(medical));

        employeeDao.save(employee);

        displayEmployeeListIntoTable();
    }//GEN-LAST:event_jButton1ActionPerformed

