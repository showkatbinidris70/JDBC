    public void displayListIntoTable() {
        clearTable();
        SummaryDao summaryDao = new SummaryDaoImpl();
        ProductDao productDao = new ProductDaoImpl();

        List<Summary> list = summaryDao.getList();
        DefaultTableModel model = (DefaultTableModel) tblDisplaySales.getModel();
        Object[] cols = new Object[5];

        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getProductName();
            cols[1] = list.get(i).getAvailableQty();
            cols[2] = list.get(i).getQuantity();
            cols[3] = list.get(i).getSoldQty();
            cols[4] = list.get(i).getTotalQty();

            Product product = productDao.getProductByProductName(list.get(i).getProductName());

            model.addRow(cols);
        }
    }

