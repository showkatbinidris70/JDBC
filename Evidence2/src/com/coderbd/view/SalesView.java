    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ProductDao pDao = new ProductDaoImpl();
        SalesDao salesDao = new SalesDaoImpl();
        Product product = pDao.getProductByProductName(txtProductName.getText().trim());

        Sales sales = new Sales(txtProductName.getText().trim(), Integer.parseInt(txtAvailabelQty.getText().trim()), Integer.parseInt(txtSalesQty.getText().trim()), Double.parseDouble(txtUnitOPrice.getText().trim()), Double.parseDouble(txtTotalPrice.getText().trim()));
        salesDao.save(sales);

        SummaryDao summaryDao = new SummaryDaoImpl();

        Summary summary = summaryDao.getProductByProductName(sales.getProductName());

        if (summary.getProductName() != null) {

            double totalQty = summary.getQuantity();
            double soldQty = (summary.getSoldQty() + Integer.parseInt(txtSalesQty.getText().trim()));
            double availableqty = (summary.getAvailableQty() - Integer.parseInt(txtSalesQty.getText().trim()));

            Summary summaryUp = new Summary(summary.getProductName(), 0, soldQty, totalQty, availableqty);
            summaryDao.update(summaryUp);
            JOptionPane.showMessageDialog(null, "Sales success");

        }
        displayListIntoTable();

    }//GEN-LAST:event_jButton1ActionPerformed

